public class HostelBean {
    private int rid;
    private String hostelName;
    private String hostelCode;

    /**
     * @return the rid
     */
    public int getRid() {
        return rid;
    }

    /**
     * @param rid the rid to set
     */
    public void setRid(int rid) {
        this.rid = rid;
    }

    /**
     * @return the hostelName
     */
    public String getHostelName() {
        return hostelName;
    }

    /**
     * @param hostelName the hostelName to set
     */
    public void setHostelName(String hostelName) {
        this.hostelName = hostelName;
    }

    /**
     * @return the hostelCode
     */
    public String getHostelCode() {
        return hostelCode;
    }

    /**
     * @param hostelCode the hostelCode to set
     */
    public void setHostelCode(String hostelCode) {
        this.hostelCode = hostelCode;
    }
}
